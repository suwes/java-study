package com.java;
public class Main {
    public static void main(String[] args){
        
        // Jenis Jenis tipe data di java
        // Integer, Byte, Short, Long, Double, Float, Char, Boolean
        
        
        // integer (satuan)
        int i = 10;
        System.out.println("======== INTEGER ========");
        System.out.println("nilai integer i = " + i);
        System.out.println("nilai MAX = " + Integer.MAX_VALUE);
        System.out.println("nilai MIN = " + Integer.MIN_VALUE);
        System.out.println("nilai besar int = " + Integer.BYTES + " byte");
        System.out.println("nilai ukuran int = " + Integer.SIZE + " bit");
    
        // byte (satuan)
        byte b = 10;
        System.out.println("======== BYTE ========");
        System.out.println("nilai byte i = " + b);
        System.out.println("nilai MAX = " + Byte.MAX_VALUE);
        System.out.println("nilai MIN = " + Byte.MIN_VALUE);
        System.out.println("nilai besar byte = " + Byte.BYTES + " byte");
        System.out.println("nilai ukuran byte = " + Byte.SIZE + " bit");
        
        // short (satuan)
        short s = 10;
        System.out.println("======== SHORT ========");
        System.out.println("nilai short i = " + s);
        System.out.println("nilai MAX = " + Short.MAX_VALUE);
        System.out.println("nilai MIN = " + Short.MIN_VALUE);
        System.out.println("nilai besar short = " + Short.BYTES + " byte");
        System.out.println("nilai ukuran short = " + Short.SIZE + " bit");
    
        // long (satuan)
        long l = 10L;
        System.out.println("======== LONG ========");
        System.out.println("nilai long i = " + l);
        System.out.println("nilai MAX = " + Long.MAX_VALUE);
        System.out.println("nilai MIN = " + Long.MIN_VALUE);
        System.out.println("nilai besar long = " + Long.BYTES + " byte");
        System.out.println("nilai ukuran long = " + Long.SIZE + " bit");
        
        // double (ada koma, bilangan real)
        double d = 10.5d;
        System.out.println("======== DOUBLE ========");
        System.out.println("nilai Double  = " + d);
        System.out.println("nilai MAX = " + Double.MAX_VALUE);
        System.out.println("nilai MIN = " + Double.MIN_VALUE);
        System.out.println("nilai besar Double = " + Double.BYTES + " byte");
        System.out.println("nilai ukuran Double = " + Double.SIZE + " bit");
    
        // float (ada koma, bilangan real)
        float f = 10.5f;
        System.out.println("======== FLOAT ========");
        System.out.println("nilai Float  = " + f);
        System.out.println("nilai MAX = " + Float.MAX_VALUE);
        System.out.println("nilai MIN = " + Float.MIN_VALUE);
        System.out.println("nilai besar Float = " + Float.BYTES + " byte");
        System.out.println("nilai ukuran Float = " + Float.SIZE + " bit");
        
        // char (Karakter)Berdasarkan ASCII
        char c = 'c';
        System.out.println("======== CHAR ========");
        System.out.println("nilai Char  = " + c);
        System.out.println("nilai MAX = " + Character.MAX_VALUE);
        System.out.println("nilai MIN = " + Character.MIN_VALUE);
        System.out.println("nilai besar Char = " + Character.BYTES + " byte");
        System.out.println("nilai ukuran Char = " + Character.SIZE + " bit");
        
        // boolean (Nilai true atau false)
        boolean val = true;
        System.out.println("======== BOOLEAN ========");
        System.out.println("nilai Boolean = " + val);
        System.out.println("nilai MAX = " + Boolean.TRUE);
        System.out.println("nilai MIN = " + Boolean.FALSE);
       
        
        
    }
}
