package com.tutorial;

public class Main {
    public static void main(String[] args){
        // ini adalah koment
        // mambuat variable dengan nama a
        int a = 10; // assignment 10 adalah a
        System.out.println("nilai a = " + a);
        // ganti nilai variable
        a = 20;
        System.out.println("Nilai a baru = " + a);
        
        // kita akan membuat sebuah deklarsi
        int b; // deklarasi
        
        b = 10;
        System.out.println("Nilai b = " + b);
    }
}
